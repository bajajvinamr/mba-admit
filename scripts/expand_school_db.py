def make_full_entry(base: dict) -> dict:
    """Fill in standard fields for a school entry."""
    entry = dict(base)

    # Ensure all standard fields exist
    defaults = {
        "essay_prompts": [
            f"Why do you want to pursue a {entry.get('degree_type', 'MBA')} at this stage of your career?",
            "Describe a time when you demonstrated leadership.",
            "What unique perspective will you bring to the program?"
        ],
        "admission_requirements": {
            "gmat_gre": f"Average GMAT: {entry.get('gmat_avg', 'N/A')} (GRE accepted)",
            "work_experience": "0-2 years" if entry.get("degree_type") == "MiM" else "3-5 years recommended",
            "english_proficiency": "TOEFL 100+ / IELTS 7.0+",
            "transcripts": "Official transcripts required",
            "recommendations": "2 recommendations required",
            "resume": "CV required",
        },
        "program_details": {
            "duration": entry.get("program_duration", "24 months"),
            "format": "Full-Time, In-Person",
            "class_size": entry.get("class_size", 100),
            "stem_designated": entry.get("stem_designated", False),
        },
        "unique_features": [],
        "placement_stats": {
            "median_base_salary_usd": None,
            "median_signing_bonus_usd": None,
            "employment_rate_3mo_pct": None,
            "top_industries": None,
            "top_employers": None,
        },
        "admission_deadlines": [
            {"round": "Round 1", "deadline": "October 2025", "decision": "December 2025"},
            {"round": "Round 2", "deadline": "January 2026", "decision": "March 2026"},
        ],
        "application_questions": entry.get("essay_prompts", []),
    }

    for key, val in defaults.items():
        if key not in entry:
            entry[key] = val

    return entry


